package bibliotecas;


public class TestaMath1 {

	public static void main(String[] args) {
		
		
		System.out.println("Função Random");
		
		System.out.println(Math.random());
		//Retorna numeros aleatorios
		
		System.out.println("Função Max");
		
		int a = 19;
		int b = 12;
		System.out.println(Math.max(b,a));
		//A função max retorna o maior valor de 2 numeros.
		
		System.out.println("Função Min");
		
		double c = 14;
		double d = 36;
		
		System.out.println(Math.min(c, d));
		// Retorna o menor laor de dois numeros
		
		
		System.out.println("Função Ceil");
		
		double n = 2.5;
		
		System.out.println(Math.ceil(n));
		//Converte para um numero inteiro seguinte
		
		
		System.out.println("Função Floor");
		
		double z = 8.9;
		
		System.out.println(Math.floor(z));
		// Converte para um numero inteiro para baixo
		
	

	}

}
