package bibliotecas;

public class TestaMath2 {

	public static void main(String[] args) {
		
		System.out.println("Função exp");
		
		int x = 4;
	
		System.out.println(Math.exp(x));
		//Função exp
		
		
		System.out.println("Função Log");
		double t = 5;
		
		System.out.println(Math.log(t));
		//retorna o logaritmo natural(base e) de um número
		
		
		System.out.println("Função Pow");		
		int a = 2;
		int b = 3;
		System.out.println(Math.pow(a, b));
		//Função que utiliza a (base,expoente)
		
		//
		//
		
		System.out.println("Sqrt");
		int p = 9;	
		System.out.println(Math.sqrt(p));
		//Retorna a raiz quadrada
		
	
		
		System.out.println("Função Sin");
		double y = 10;
		System.out.println(Math.sin(y));
		// Retorna um numero seno 
		
		
		System.out.println("Função toDegrees");
		double z = 10;
		System.out.println(Math.toDegrees(z));
		//Conversão de Radiano para Graus
		
		
		System.out.println("Função to Radians");
		double u = 236;
		System.out.println(Math.toRadians(u));
		//Converte um angulo em graus para radiano
		
	}

}
